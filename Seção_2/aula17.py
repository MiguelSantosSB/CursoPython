# Operadores Basicos
print('adição', 10 + 10)
print('subtração', 10 - 7)
print('multiplicação', 10 * 10)
print(5 * 'miguel')  # Pode ser utilizada com outros tipo de dado
print('divisão', 10 / 3)

# Operadores
print('divisão inteira', 10 // 3)
print('Exponenciação/Potenciação', 2 ** 3)
print('Resto da divisão', 10 % 2)
print('Presedencia', 5 + 2 * 10)  # errada
print('Presedencia', (5 + 2) * 10)  # certa